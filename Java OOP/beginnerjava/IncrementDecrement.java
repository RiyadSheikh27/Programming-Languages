
package beginnerjava;


public class IncrementDecrement {
    public static void main(String[] args) {
        int x = 25;
        int y;
        
        y = --x;
        System.out.println("Y = "+y);
        
        y = x--;
        System.out.println("Y = "+y);
        
        y = ++x;
        System.out.println("Y = "+y);
        
        y = x++;
        System.out.println("Y = "+y);
        
              
    }
    
}
